package restdemospring.models;

public class Shoes {

    int size;
    String color;
    int id;

    public void setSize(int size) {
        this.size = size;
    }

    public String getColor() {
        return color;
    }

    public int getId() {
        return id;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSize() {
        return size;
    }
}
